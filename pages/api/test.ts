const prisma = new PrismaClient();

export default async function handler(
  req: NextApiRequest,
  res: NextApiResponse
) {
  const user = await prisma.user.findFirst({
    where: { whatsapp: '+6285171232449' },
  });

  const accounts = await prisma.account.findFirst({
    where: { kudosId: user!.id },
  });

  const transactions = await prisma.transaction.findMany({
    where: { institution_id: accounts?.institutionId },
  });

  const twentytwentytwo = _.filter(transactions, ({ dateTimestamp }) => {
    if (!dateTimestamp) throw new Error('ngentot');
    return new Date(dateTimestamp).getFullYear() === 2022;
  });

  const month = Object.keys(
    _.groupBy(transactions, ({ dateTimestamp }) => {
      if (!dateTimestamp) return;

      return new Date(dateTimestamp).getMonth();
    })
  );

  const year = Object.keys(
    _.groupBy(transactions, ({ dateTimestamp }) => {
      if (!dateTimestamp) return;

      const year = new Date(dateTimestamp).getFullYear();

      switch (year) {
        case 2022:
          return 0;

        case 2023:
          return 1;

        case 2024:
          return 2;

        default:
          return 0;
      }
    })
  );

  res.status(200).json(year);
}

